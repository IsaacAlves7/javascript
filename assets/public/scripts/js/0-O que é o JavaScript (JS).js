/* ------------------------ Servidor e Cliente ------------------------

A internet é uma rede de computadores e obtemos arquivos através da Web.
Em desenvolvimento para a Web sempre separamos dois setores: Servidor e a Cliente. 

O cliente é o computador do usuário, assim como o seu. É utilizado pelas pessoas para 
acessar sites ou sistemas a partir de um endereço (Google Chrome, Internet Explorer, Safari, Mozilla Firefox e etc).

Os arquivos vindos por esse endereço são disponibilizados por um outro computador que serve estes arquivos. 
Estes computadores que estão em algum lugar do mundo que servem arquivos e outros serviços são chamados de
servidores (Apache, IIS, Glassfish e etc).

Nos servidores programamos com linguagens como PHP, Java, C#, Ruby e entre outros. E também gerenciamos banco de dados (MySQL, SQ Lite, Firebase, MongoDB, SQL Server e etc), 
onde chamamos essa parte de back-end. Quando há acesso ao banco de dados, o servidor trabalha de uma forma diferenciada, pois a implementação de um servidor web é um serviço
diferente à implementação de um servidor de banco de dados.

Os clientes, que podem ser qualquer dispositivo conectado à Internet como smartphones, notebooks, tablets e smart-TVS, recebem arquivos desses servidores, como imagens,
notebooks, vídeos, arquivos HTML, CSS e JavaScript. Chamamos essa parte de front-end.

Atualmente, o JavaScript domina as duas partes, front-end e back-end! Ou seja dá pra desenvolver muita coisa com apenas uma linguagem! O que é bastante produtivo e eficiente.
*/

